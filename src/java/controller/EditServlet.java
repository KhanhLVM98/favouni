    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String username = request.getParameter("username");
        User u = new User(username);
        HttpSession ss = request.getSession();
        request.getSession().setAttribute("ue", u);
        request.getRequestDispatcher("edit.jsp").include(request, response);
        response.setContentType("text/html;charset=UTF-8");     
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession ss = request.getSession();
        User u = (User)ss.getAttribute("ue");
        String fullname = request.getParameter("fullname");
        String dob = request.getParameter("dob");
        String gender = request.getParameter("gender");
        String email = request.getParameter("email");
        String phone = request.getParameter("phone");
        String userLocation = request.getParameter("userLocation");
        String bio = request.getParameter("bio");
        User uu = new User(u.getUsername(),fullname,u.getUserpass(),dob,gender.equals("M"),email,phone,userLocation,bio);
        if(uu.updateUser(uu)) {
            request.getRequestDispatcher("profile.jsp?username=\""+u.getUsername()+"\"").include(request, response);
        }else{
            request.getRequestDispatcher("edit.jsp?error=0").include(request, response);
        }
        response.setContentType("text/html;charset=UTF-8");
    }

